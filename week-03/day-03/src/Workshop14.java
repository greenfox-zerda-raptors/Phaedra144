public class Workshop14 {
    public static void main(String[] args) {
        // Reverse the order of ah
        int[] ah = new int[]  { 3, 4, 5, 6, 7 };

        for (int i = ah.length-1; i >= 0; i--) {
            System.out.print(ah[i] + " ");
        }

    }
}

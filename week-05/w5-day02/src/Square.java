public class Square extends Rectangle {

    public Square(int width) {
        this.setHeight(width);
        this.setWidth(width);
    }


    @Override
    public int getArea(int width, int height) {
        return super.getArea(width, height);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
